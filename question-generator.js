// Question Generator - Brain Trainer
// AI-powered automatic question generation

(function() {
    'use strict';

    /**
     * Generate questions for a document using AI
     * @param {string} documentId - The document ID
     * @param {string} documentTitle - The document title
     * @param {string} documentContent - The document content
     * @returns {Promise<Array>} Array of generated questions
     */
    async function generateQuestions(documentId, documentTitle, documentContent) {
        const systemPrompt = `You are an expert educator creating quiz questions for finance professionals.
Generate questions that test genuine understanding, not just memorization.
Always respond with valid JSON only, no other text.`;

        // Truncate content if too long
        const maxContentLength = 8000;
        const truncatedContent = documentContent.length > maxContentLength
            ? documentContent.substring(0, maxContentLength) + '...[truncated]'
            : documentContent;

        const userMessage = `
Create 5 quiz questions for this educational content about finance/investing.

DOCUMENT TITLE: ${documentTitle}

DOCUMENT CONTENT:
${truncatedContent}

Generate questions at varying difficulty levels:
- Level 1-2: Definition/recognition (what is X?)
- Level 3-4: Application (calculate X, what happens if Y?)
- Level 5: Synthesis (how does X relate to Y in situation Z?)

Respond with this exact JSON structure:
{
  "questions": [
    {
      "id": "auto-1",
      "level": 1,
      "type": "self_grade",
      "question": "What is...?",
      "validation": {
        "method": "llm",
        "keyPoints": ["key point 1", "key point 2", "key point 3"],
        "minPointsForCorrect": 2,
        "minPointsForPartial": 1
      }
    },
    {
      "id": "auto-2",
      "level": 3,
      "type": "numeric",
      "question": "If X is 100 and Y is 5%, what is Z?",
      "validation": {
        "method": "numeric",
        "expected": 105,
        "tolerance": 0
      }
    },
    {
      "id": "auto-3",
      "level": 5,
      "type": "self_grade",
      "question": "Explain how X affects Y when Z happens...",
      "validation": {
        "method": "llm",
        "keyPoints": ["point 1", "point 2", "point 3", "point 4"],
        "minPointsForCorrect": 3,
        "minPointsForPartial": 2
      }
    }
  ]
}

Include a mix of numeric (where calculations are possible) and conceptual questions.
For conceptual questions, provide 2-4 key points that a correct answer should cover.
Make questions specific to the content - avoid generic questions.
`;

        try {
            const result = await ApiClient.callClaudeJSON(systemPrompt, userMessage);

            if (!result.questions || !Array.isArray(result.questions)) {
                throw new Error('Invalid response format');
            }

            // Add document reference and generate proper IDs
            result.questions.forEach((q, index) => {
                q.id = 'q_' + Date.now().toString(36) + '_' + index;
                q.sourceDocument = documentId;
                q.autoGenerated = true;

                // Ensure validation method is set correctly
                if (q.type === 'numeric' && q.validation) {
                    q.validation.method = 'numeric';
                } else if (q.validation && q.validation.keyPoints) {
                    q.validation.method = 'llm';
                }
            });

            return result.questions;
        } catch (error) {
            console.error('Question generation failed:', error);
            throw error;
        }
    }

    /**
     * Check if AI question generation is available
     * @returns {boolean} True if available
     */
    function isAvailable() {
        return CONFIG.aiEnabled() && CONFIG.getAiSettings().autoGenerateQuestions;
    }

    // Export
    window.QuestionGenerator = {
        generateQuestions,
        isAvailable
    };

})();
